package com.botqa.model;

public class BotqaVO implements java.io.Serializable{
	private Integer keywordID;
	private String keywordName;
	private String keywordContext;
	public Integer getKeywordID() {
		return keywordID;
	}
	public void setKeywordID(Integer keywordID) {
		this.keywordID = keywordID;
	}
	public String getKeywordName() {
		return keywordName;
	}
	public void setKeywordName(String keywordName) {
		this.keywordName = keywordName;
	}
	public String getKeywordContext() {
		return keywordContext;
	}
	public void setKeywordContext(String keywordContext) {
		this.keywordContext = keywordContext;
	}
	
	
}
